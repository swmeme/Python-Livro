

'''
Programa que imprime números inteiros entre 1 e um valor
digitado pelo usuário para estudo de contadores
'''


fim = int(input('Digite o ultimo número: '))
x = 1                               #contador
while x <= fim:
    print(x)
    x += 1                #contador + 1 até que a condição while não seja False


'''
Um contador é uma variável usada para contar o número de ocorrências de um determinado evento; 
nesse caso, o número de repetições do while, que satisfaz às necessidades do problema
'''